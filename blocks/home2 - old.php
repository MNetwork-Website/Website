<div class="home-intro">
<h1>VISION</h1>
<p>A Canada where people are connected to each other, their communities, and the resources they need to create a better future for themselves and the world.</p>
<h1>MISSION</h1>
<p>We will realize our vision for Canada's future by strengthening the two most pervasive & powerful levers of system-wide transformation: The Who (People) & the How (Process). Specifically, we work to:<p>
<p><i>Recognize, connect, develop, and enable an interdisciplinary learning community of Canada's change leaders <br/>(The Who)</i></p>
<p><i>Develop, promote and embed practices and processes that enable people and organizations to create more purposeful impact in the world. <br/>(The How)</i></p>
<div class="borderdiv2">
</div>
<h1>WHAT IS THE M-NETWORK?</h1>
  <h6>We are a national movement of thousands - a community of leaders across the country with a desire to build a better future for Canada.</h6>
<div class="home-intro-columns">
<div class="home-left-column">
<h3>WHO WE ARE</h3>
<ul>
<li>We are the <strong>co-founders of international development projects</strong> and human rights advocacy groups.</li>
<li>We are <strong>CEOs of social enterprises</strong> that focus on a triple bottom line.</li>
<li>We are the linchpins, <strong>the tipping points</strong>, the disturbers of the status quo.</li>
<li>We are musicians, poets, and artists <strong>armed with messages that inspire</strong>, question, and empower.</li>
</ul>
</div>
<div class="home-right-column">
<h3>WHAT WE BELIEVE</h3>
<p>We believe that <strong>people</strong> are the most important drivers of change.</p>
<p>We also believe that everything we need to create the future we desire for Canada already exists - it’s just a matter of the different pieces (people, organizations, knowledge, and resources) being a lot more connected to one another than they are today. </p>

<p>To make it all a reality, we believe we need a different approach to the work we do - one that’s a lot more more focused on the ultimate impact, is driven through people and relationships, and learns through constant experimentation and reflection.</p>
</div>
