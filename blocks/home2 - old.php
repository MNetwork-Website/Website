<h1>WHAT DOES THE M-NETWORK DO?</h1>
<h6>In short: Leadership Development. Impact Effectiveness.</h6>
<p>We recognize, connect, develop, and enable Canada’s emerging leaders.  We also advocate for and embed more effective ways of creating impact into what we do.</p>
