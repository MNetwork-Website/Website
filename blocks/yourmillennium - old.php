  <h3>You have told us what you loved about Millennium was the people, the connections, the support, the resources, the people, and the connections.</h3>
  <p>We listened; and in 2012, you can expect a lot more of just what you loved:</p>
  <h4>M-Jams</h4>
  <p>On the last Saturday of every month, from the comfort of your PJs, an M-Jam gives you the opportunity to reconnect with old friends and meet new like-minded people from across the country, laugh together, learn from one another, and share stories!</p>
  <h4>Local Hubs</h4>
  <p>It's where the magic happens - connecting with incredible people around great food and in a comfortable space...from social events to community projects, this is where M becomes real in your home community</p>
  <h4>Conferences</h4>
  <p>10x the connections, 100x the magic, 1000x the spontaneous late-night conversations. There isn't anything like it.</p>
  <h3>You also told us that we should also work to provide the same experiences and opportunities for other emerging leaders in Canada - so we are introducing...</h3>
  <h4>M-Awards</h4>
  <p>A national award that recognizes and connects our country's emerging leaders and supports them as they create the extraordinary </p>
