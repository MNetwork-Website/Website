<h1>"Millennium Changed My Life"</h1>
<p><strong><abbr title="(those of you I haven't met yet and those of you who have turned into a mix of my inspirations, my heroes, my best friends, and my most memorable 2:30AM conversations)">Laureates,</abbr></strong></p>
<p>At the turn of the new millennium, a decision was made to invest into something big - the Canada Millennium Scholarship Foundation.</p>

<p>One part of the Foundation was the Excellence Awards Program - and wow, was it worlds more than a Program and was it certainly more than just an Award!</p>

<p>It was a national experiment in transforming Canada's future through investing in great people, bringing them together, and giving them opportunities to dream, learn, and share. From the Awards to the Chapters to the Conferences to the Grants, we can only marvel at things we all learned.</p>
