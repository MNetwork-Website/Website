<p>There is a whole book on the details of what the Foundation did <a href="http://www.dmpibooks.com/book/a-million-futures" target="_blank">(check out Silver Donald Cameron&rsquo;s great work, A Million Futures)</a> during its 10 years in existence but there is something many of us felt and expressed time after time. It changed our views, our beliefs, our paths, and our ways of dealing with the world.<br />
  <br />
  In short, many of us felt that Millennium - nay, the people of Millennium - <strong>changed our lives</strong>.<br />
  <br />
  On his last day at the Foundation before it closed, <strong>Chad Lubelsky</strong>, a Foundation stalwart and the catalyst behind much of the awesomeness that happened, wrote these words in a note borrowing with gratitude and apology from Boston the band:<br />
  <br />
  <em>I looked out this morning and the sun was gone<br />
  Turned on some music to start my day<br />
  I lost myself in a familiar song<br />
  I closed my eyes and I slipped away<br />
  <br />
  It's more than a feeling<br />
  (More than a feeling)<br />
  When I hear that old song they used to play<br />
  (More than a feeling)<br />
  I begin dreaming<br />
  (More than a feeling)<br />
  'Til I see Millennium walk away<br />
  I see my Millennium walkin' away</em><br />
  <br />
  It definitely was more than a feeling - but we think Millennium only walked away for a sabbatical and<strong> it&rsquo;s time to really bring the amazing back!</strong><br />
  <br />
  It&rsquo;s high time we bring back what we loved about it, reconnect as a community, and share it with other emerging leaders across the country<br />
  </p>
