<h3>National Fund Development Lead</h3>
<p>You have experience with significant fund development efforts with both granting community and private sector organizations. You have experience with developing pitch materials and relationships to leverage.</p>

<h3>M-Awards Team</h3>
<p>You have connections in your community, willing to do whatever it takes to make the Awards take off, and are a master at execution! You are excited about getting the Awards off the ground and being a part of selecting Canada's change leaders.</p>

<h3>Creative Team</h3>
<p>You have solid visual design, publishing, and creative skills - especially on a computer. You love telling a story through powerful visuals, or summarizing many paragraphs into one single powerful image.</p>

<h3>M-Live Website Team (development and programming)</h3>
<p>You have strong development and programming skills (PHP/MySQL/Javascript/HTML/CSS) and are interested in building a dynamic, crowdsourced, and analytics conversation platform that will be used by hundreds of leaders across the country</p>

<h3>Francophone Community Team</h3>
<p>You are fluent in French, have a passion for the language as well as building our French communities, and telling the M story in French across all channels</p>

